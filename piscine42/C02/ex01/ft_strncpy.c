char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

#include <stdio.h>
int main(){
	char src[] = "sedef";
	char dest[3];
	printf("%s", ft_strncpy(dest,src,3));
	return 0;
}
